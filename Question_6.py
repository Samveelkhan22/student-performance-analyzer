def letter_grade(average):
    if average >= 90:
        print("Your letter grade is A")
    if 80 <= average < 90:
        print("Your letter grade is B")
    if 70 <= average < 80:
        print("Your letter grade is C")
    if 60 <= average < 70:
        print("Your letter grade is D")
    if average < 60:
        print("Your letter grade is F")

